
-- Divide and Conquer

  # Is one of the algorithm design paradigm

  # What do we do?
    + The idea is to first break the problem into smaller problems
      which then gets solved recursively, and then to somehow quickly
      combine the solutions to the sub problems into one for the
      original problem that you actually care about.

  # We can apply this for?
    + Integer multiplication
    + Sorting
    + Matrix multiplication
    + Closest pair
    + Nearest neighbour type
    + etc...
