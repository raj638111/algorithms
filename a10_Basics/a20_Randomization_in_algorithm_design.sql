
-- Randomized algorithm

  # What does it do?
    + Will actually have different executions if you run it over and
      over again on a fixed input

  # Why randomize?
    + Allowing randomization internal to an algorithm, often leads to simple,
      elegant, and practical solution to various computational problems

  # Where does randomization used?
    + Randomized quick sort
    + Randomized primality testing
    + Randomized approach to graph partitioning
    + Hash functions & Hash maps